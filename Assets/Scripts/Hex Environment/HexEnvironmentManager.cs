    private GameObject GetPrefabForHexType(HexType type)
    {
        return type switch
        {
            HexType.CenterHub => centerHubPrefab ?? hexPrefab,
            HexType.Pathway => pathwayPrefab ?? hexPrefab,
            HexType.DefenderSpot => defenderSpotPrefab ?? hexPrefab,
